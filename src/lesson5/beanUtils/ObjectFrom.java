package lesson5.beanUtils;

public class ObjectFrom {
    private int aFrom;
    private int bFrom;
    private int cFrom;
    private String dFrom;

    public ObjectFrom(int a, int b, int c, String d){
        this.aFrom = a;
        this.bFrom = b;
        this.cFrom = c;
        this.dFrom = d;
    }

    public int getaFrom() {
        return aFrom;
    }

    public int getbFrom() {
        return bFrom;
    }

    public String getdFrom() {
        return dFrom;
    }
}
